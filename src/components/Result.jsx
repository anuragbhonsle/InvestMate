import {
  LineChart,
  Line,
  XAxis,
  YAxis,
  Tooltip,
  CartesianGrid,
  ResponsiveContainer,
  Legend,
} from "recharts";
import { formatter } from "../util/investment";

export default function Result({ input, data }) {
  if (!input) return null;

  if (!data || data.length === 0) {
    return (
      <div className="rounded-2xl border border-zinc-300 bg-white/90 backdrop-blur-sm p-8 text-center shadow-md">
        <h2 className="mb-2 text-xl font-semibold text-zinc-900">
          Investment projection
        </h2>
        <p className="text-sm text-zinc-600">
          Enter a duration of at least 1 year to see how your investment could
          grow over time.
        </p>
      </div>
    );
  }

  const finalYear = data[data.length - 1];
  const totalInvested =
    input.initialInvestment + input.annualInvestment * input.duration;
  const totalInterest = finalYear.valueEndOfYear - totalInvested;
  const roi =
    totalInvested > 0 ? (totalInterest / totalInvested) * 100 : Number.NaN;

  const chartData = data.map((entry) => ({
    ...entry,
    totalInvestedSoFar:
      input.initialInvestment + input.annualInvestment * entry.year,
  }));

  return (
    <div className="space-y-8">
      {/* Header */}

      {/* Summary cards */}
      <div
        className="grid grid-cols-1 gap-4 sm:grid-cols-2 lg:grid-cols-4"
        aria-label="Investment summary"
      >
        <SummaryCard
          title="Final Balance"
          value={formatter.format(finalYear.valueEndOfYear)}
          label="Projected value after all years"
        />

        <SummaryCard
          title="Total Invested"
          value={formatter.format(totalInvested)}
          label="Principal contributed (initial + yearly)"
        />

        <SummaryCard
          title="Total Interest"
          value={formatter.format(totalInterest)}
          label="Growth generated by compound returns"
          accent
        />

        <SummaryCard
          title="ROI"
          value={Number.isFinite(roi) ? `${roi.toFixed(1)}%` : "â€”"}
          label="Return on investment over the full period"
        />
      </div>

      {/* Chart */}
      <div className="rounded-2xl border border-zinc-300 bg-white/85 backdrop-blur-sm p-5 shadow-lg">
        <ResponsiveContainer width="100%" height={320}>
          <LineChart
            data={chartData}
            margin={{ top: 20, right: 30, bottom: 5, left: 0 }}
          >
            <CartesianGrid strokeDasharray="3 3" stroke="#d4d4d8" />

            <XAxis
              dataKey="year"
              stroke="#18181b"
              tick={{ fill: "#18181b", fontSize: 12 }}
            />

            <YAxis stroke="#18181b" tick={{ fill: "#18181b", fontSize: 12 }} />

            <Tooltip
              formatter={(value) => formatter.format(value)}
              contentStyle={{
                backgroundColor: "#18181b",
                borderRadius: "10px",
                border: "1px solid #52525b",
                color: "#fafafa",
                boxShadow: "0 10px 25px rgba(0,0,0,0.35)",
              }}
              labelStyle={{ color: "#a1a1aa" }}
            />

            <Line
              type="monotone"
              dataKey="totalInvestedSoFar"
              stroke="#38bdf8"
              strokeWidth={2.5}
              dot={false}
              name="Total invested"
            />

            <Line
              type="monotone"
              dataKey="valueEndOfYear"
              stroke="#22c55e"
              strokeWidth={2.5}
              dot={false}
              activeDot={{ r: 5 }}
              name="Portfolio value"
            />

            <Legend
              wrapperStyle={{
                color: "#18181b",
                fontSize: "13px",
              }}
            />
          </LineChart>
        </ResponsiveContainer>
      </div>

      {/* Table */}
      <div
        className="overflow-x-auto rounded-2xl border border-zinc-300 bg-white/85 backdrop-blur-sm shadow-md"
        aria-label="Year-by-year breakdown"
      >
        <table className="min-w-full text-sm">
          <thead className="bg-zinc-100 text-zinc-800">
            <tr>
              <th className="px-4 py-3 text-left font-semibold">Year</th>
              <th className="px-4 py-3 text-left font-semibold">Interest</th>
              <th className="px-4 py-3 text-left font-semibold">
                Annual Investment
              </th>
              <th className="px-4 py-3 text-left font-semibold">
                Total Invested So Far
              </th>
              <th className="px-4 py-3 text-left font-semibold">
                End of Year Value
              </th>
            </tr>
          </thead>
          <tbody className="divide-y divide-zinc-200 text-zinc-900">
            {chartData.map((d) => (
              <tr key={d.year} className="hover:bg-zinc-50">
                <td className="px-4 py-3">{d.year}</td>
                <td className="px-4 py-3">{formatter.format(d.interest)}</td>
                <td className="px-4 py-3">
                  {formatter.format(d.annualInvestment)}
                </td>
                <td className="px-4 py-3">
                  {formatter.format(d.totalInvestedSoFar)}
                </td>
                <td className="px-4 py-3 font-semibold">
                  {formatter.format(d.valueEndOfYear)}
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>
    </div>
  );
}

function SummaryCard({ title, value, label, accent }) {
  return (
    <div className="rounded-2xl border border-zinc-300 bg-white/85 backdrop-blur-sm p-4 shadow-md">
      <h3 className="text-sm font-medium text-zinc-600">{title}</h3>

      <p
        className={`mt-1 text-2xl font-semibold ${
          accent ? "text-emerald-600" : "text-zinc-900"
        }`}
      >
        {value}
      </p>

      <p className="mt-1 text-xs text-zinc-500">{label}</p>
    </div>
  );
}

import {
  LineChart,
  Line,
  XAxis,
  YAxis,
  Tooltip,
  CartesianGrid,
  ResponsiveContainer,
  Legend,
} from "recharts";
import { formatter } from "../util/investment";

export default function Result({ input, data }) {
  if (!input) return null;

  if (!data || data.length === 0) {
    return (
      <div className="result-empty">
        <h2 className="center">Investment projection</h2>
        <p>
          Enter a duration of at least 1 year to see how your investment could
          grow over time.
        </p>
      </div>
    );
  }

  const finalYear = data[data.length - 1];
  const totalInvested =
    input.initialInvestment + input.annualInvestment * input.duration;
  const totalInterest = finalYear.valueEndOfYear - totalInvested;
  const roi =
    totalInvested > 0
      ? (totalInterest / totalInvested) * 100
      : Number.NaN;

  const chartData = data.map((entry) => ({
    ...entry,
    totalInvestedSoFar:
      input.initialInvestment + input.annualInvestment * entry.year,
  }));

  return (
    <div>
      <div className="result-header">
        <h2 className="section-title">See how your investment could grow</h2>
      </div>

      <div className="summary-grid" aria-label="Investment summary">
        <div className="summary-card">
          <h3>Final Balance</h3>
          <p className="summary-value">
            {formatter.format(finalYear.valueEndOfYear)}
          </p>
          <p className="summary-label">Projected value after all years</p>
        </div>
        <div className="summary-card">
          <h3>Total Invested</h3>
          <p className="summary-value">{formatter.format(totalInvested)}</p>
          <p className="summary-label">
            Principal contributed (initial + yearly)
          </p>
        </div>
        <div className="summary-card">
          <h3>Total Interest</h3>
          <p className="summary-value accent">
            {formatter.format(totalInterest)}
          </p>
          <p className="summary-label">Growth generated by compound returns</p>
        </div>
        <div className="summary-card">
          <h3>ROI</h3>
          <p className="summary-value">
            {Number.isFinite(roi) ? `${roi.toFixed(1)}%` : "â€”"}
          </p>
          <p className="summary-label">
            Return on investment over the full period
          </p>
        </div>
      </div>

      <div className="chart-wrapper">
        <ResponsiveContainer width="100%" height={320}>
          <LineChart
            data={chartData}
            margin={{ top: 20, right: 30, bottom: 5, left: 0 }}
          >
            <CartesianGrid
              strokeDasharray="3 3"
              stroke="rgba(255,255,255,0.08)"
            />
            <XAxis dataKey="year" stroke="#c2e9e0" />
            <YAxis stroke="#c2e9e0" />
            <Tooltip
              formatter={(value) => formatter.format(value)}
              contentStyle={{
                backgroundColor: "rgba(0,0,0,0.85)",
                borderRadius: "8px",
                border: "1px solid rgba(255,255,255,0.15)",
              }}
            />
            <Line
              type="monotone"
              dataKey="totalInvestedSoFar"
              stroke="#38bdf8"
              strokeWidth={2}
              dot={false}
              name="Total invested"
            />
            <Line
              type="monotone"
              dataKey="valueEndOfYear"
              stroke="#22c55e"
              strokeWidth={2}
              dot={false}
              activeDot={{ r: 5 }}
              name="Portfolio value"
            />
            <Legend />
          </LineChart>
        </ResponsiveContainer>
      </div>

      <div className="table-wrapper" aria-label="Year-by-year breakdown">
        <table>
          <thead>
            <tr>
              <th>Year</th>
              <th>Interest</th>
              <th>Annual Investment</th>
              <th>Total Invested So Far</th>
              <th>End of Year Value</th>
            </tr>
          </thead>
          <tbody>
            {chartData.map((d) => (
              <tr key={d.year}>
                <td>{d.year}</td>
                <td>{formatter.format(d.interest)}</td>
                <td>{formatter.format(d.annualInvestment)}</td>
                <td>{formatter.format(d.totalInvestedSoFar)}</td>
                <td>{formatter.format(d.valueEndOfYear)}</td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>
    </div>
  );
}
